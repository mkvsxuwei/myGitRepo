package com.entity;

public class User {

	private String home;
	private int id;
	private String info;
	private String name;
	private String pwd;
	private String sex;
	public String getHome() {
		return home;
	}
	public int getId() {
		return id;
	}
	public String getInfo() {
		return info;
	}
	public String getName() {
		return name;
	}
	public String getPwd() {
		return pwd;
	}
	public String getSex() {
		return sex;
	}
	public void setHome(String home) {
		this.home = home;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
}
